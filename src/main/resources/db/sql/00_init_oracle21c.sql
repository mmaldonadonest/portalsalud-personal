DECLARE
  v_count NUMBER;
  PROCEDURE add_constraint_if_not_exists(
    p_table_name      IN VARCHAR2,
    p_constraint_name IN VARCHAR2,
    p_ddl             IN CLOB
  ) IS
  BEGIN
    SELECT COUNT(*) INTO v_count
      FROM USER_CONSTRAINTS
     WHERE TABLE_NAME = UPPER(p_table_name)
       AND CONSTRAINT_NAME = UPPER(p_constraint_name);

    IF v_count = 0 THEN
      EXECUTE IMMEDIATE p_ddl;
      DBMS_OUTPUT.PUT_LINE('Constraint creada: ' || p_constraint_name);
    ELSE
      DBMS_OUTPUT.PUT_LINE('Constraint existente (omitida): ' || p_constraint_name);
    END IF;
  END;
BEGIN
  -- CHECKS
  add_constraint_if_not_exists('APP_SEC_USER','CK_SEC_USER_ACTIVE',
    'ALTER TABLE APP_SEC_USER ADD CONSTRAINT CK_SEC_USER_ACTIVE CHECK (ACTIVE IN (''Y'',''N''))');

  add_constraint_if_not_exists('APP_SEC_ROLE','CK_SEC_ROLE_ACTIVE',
    'ALTER TABLE APP_SEC_ROLE ADD CONSTRAINT CK_SEC_ROLE_ACTIVE CHECK (ACTIVE IN (''Y'',''N''))');

  add_constraint_if_not_exists('APP_SEC_PERMISSION','CK_SEC_PERMISSION_ACTIVE',
    'ALTER TABLE APP_SEC_PERMISSION ADD CONSTRAINT CK_SEC_PERMISSION_ACTIVE CHECK (ACTIVE IN (''Y'',''N''))');

  add_constraint_if_not_exists('APP_MENU','CK_APP_MENU_ACTIVE',
    'ALTER TABLE APP_MENU ADD CONSTRAINT CK_APP_MENU_ACTIVE CHECK (ACTIVE IN (''Y'',''N''))');

  add_constraint_if_not_exists('APP_JOB_CATALOG','CK_JOB_CATALOG_ENABLED',
    'ALTER TABLE APP_JOB_CATALOG ADD CONSTRAINT CK_JOB_CATALOG_ENABLED CHECK (ENABLED IN (''Y'',''N''))');

  add_constraint_if_not_exists('APP_NOTIF_TEMPLATE','CK_NOTIF_TEMPLATE_ACTIVE',
    'ALTER TABLE APP_NOTIF_TEMPLATE ADD CONSTRAINT CK_NOTIF_TEMPLATE_ACTIVE CHECK (ACTIVE IN (''Y'',''N''))');

  add_constraint_if_not_exists('APP_FS_FILE_POLICY','CK_FS_FILE_POLICY_ACTIVE',
    'ALTER TABLE APP_FS_FILE_POLICY ADD CONSTRAINT CK_FS_FILE_POLICY_ACTIVE CHECK (ACTIVE IN (''Y'',''N''))');

  add_constraint_if_not_exists('APP_FS_FILE','CK_FS_FILE_STATUS',
    'ALTER TABLE APP_FS_FILE ADD CONSTRAINT CK_FS_FILE_STATUS CHECK (STATUS IN (''ACTIVE'',''DELETED'',''ARCHIVED''))');

  -- FKs
  add_constraint_if_not_exists('APP_SEC_USER_ROLE','FK_SEC_USER_ROLE_USER',
    'ALTER TABLE APP_SEC_USER_ROLE ADD CONSTRAINT FK_SEC_USER_ROLE_USER FOREIGN KEY (USER_ID) REFERENCES APP_SEC_USER(ID)');

  add_constraint_if_not_exists('APP_SEC_USER_ROLE','FK_SEC_USER_ROLE_ROLE',
    'ALTER TABLE APP_SEC_USER_ROLE ADD CONSTRAINT FK_SEC_USER_ROLE_ROLE FOREIGN KEY (ROLE_ID) REFERENCES APP_SEC_ROLE(ID)');

  add_constraint_if_not_exists('APP_SEC_ROLE_PERMISSION','FK_SEC_ROLE_PERMISSION_ROLE',
    'ALTER TABLE APP_SEC_ROLE_PERMISSION ADD CONSTRAINT FK_SEC_ROLE_PERMISSION_ROLE FOREIGN KEY (ROLE_ID) REFERENCES APP_SEC_ROLE(ID)');

  add_constraint_if_not_exists('APP_SEC_ROLE_PERMISSION','FK_SEC_ROLE_PERMISSION_PERM',
    'ALTER TABLE APP_SEC_ROLE_PERMISSION ADD CONSTRAINT FK_SEC_ROLE_PERMISSION_PERM FOREIGN KEY (PERMISSION_ID) REFERENCES APP_SEC_PERMISSION(ID)');

  add_constraint_if_not_exists('APP_MENU','FK_APP_MENU_PARENT',
    'ALTER TABLE APP_MENU ADD CONSTRAINT FK_APP_MENU_PARENT FOREIGN KEY (PARENT_ID) REFERENCES APP_MENU(ID)');

